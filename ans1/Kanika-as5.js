// Function that takes two numbers and returns their sum
function sum(a, b) {
    return a + b;
}

// Function that takes a name as an argument and returns a greeting message
function greet(name) {
    return `Hello, ${name}!`;
}

// Function that takes an array of numbers and returns the largest number
function largestNumber(numbers) {
    return Math.max(...numbers);
}

// Example usage:
console.log(sum(3, 5)); // Output: 8
console.log(greet("Kanika")); // Output: Hello, Alice!
console.log(largestNumber([1, 2, 3, 4, 5])); // Output: 5
